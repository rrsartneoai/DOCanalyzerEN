export function useTranslation(language: string) {
  const t = (key: string, params?: Record<string, string>) => {
    const translation =
      translations[language as keyof typeof translations]?.[key as keyof typeof translations.pl] ||
      translations.pl[key as keyof typeof translations.pl] ||
      key

    if (params) {
      return Object.entries(params).reduce((text, [param, value]) => {
        return text.replace(`{${param}}`, value)
      }, translation)
    }

    return translation
  }

  return { t }
}
